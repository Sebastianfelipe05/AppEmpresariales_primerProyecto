    private void btnBuscarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCarroActionPerformed
        // TODO add your handling code here:
        String placa = txtSearch.getText().trim();

        if (placa.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Por favor ingrese una placa para buscar.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        Vehiculo v = barbosa.searchVehiculo(placa);

        if (v == null) {
            JOptionPane.showMessageDialog(this,
                    "No se encontrÃ³ ningÃºn vehÃ­culo con la placa " + placa,
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (v instanceof Bus) {
            JOptionPane.showMessageDialog(this,
                    "Error: la placa " + placa + " pertenece a un Bus, no a un Carro.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (v instanceof Carro) {
            Carro c = (Carro) v;
            this.carroActual = c; // Guardamos la referencia

            // Llenamos los campos del formulario con los datos del carro encontrado
            txtMarca.setText(c.getMarca());
            txtColor.setText(c.getColor());
            txtPlaca.setText(c.getPlaca());
            txtCombustible.setText(c.getCombustible());
            txtModelo.setText(c.getModelo());
            txtAnio.setText(String.valueOf(c.getAnio())); // Convertir a String para el spinner
            txtEstado.setText(c.getEstado());
            txtNP.setText(String.valueOf(c.getNumeroPuertas()));
            txtAire.setText(c.isTieneAireAcondicionado() ? "Si" : "No");

            JOptionPane.showMessageDialog(this,
                    "Carro encontrado y cargado en el formulario.",
                    "Ãxito",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarCarroActionPerformed


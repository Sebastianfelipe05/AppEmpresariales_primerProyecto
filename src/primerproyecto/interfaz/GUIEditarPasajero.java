    public GUIEditarPasajero(Pasajero pasajero, Runnable onSaveCallback) {
        this.pasajero = pasajero;
        this.onSaveCallback = onSaveCallback;
        initComponentsK(); // inicializa los componentes

        // Cargar valores actuales del pasajero
        txtNombre.setText(pasajero.getNombre());
        txtEdad.setText(String.valueOf(pasajero.getEdad()));
    }

    @SuppressWarnings("unchecked")
    private void initComponentsK() {

        setTitle("Editar Pasajero");
        setSize(300, 200);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);

        JPanel panel = new JPanel();
        panel.setLayout(new java.awt.GridLayout(3, 2, 10, 10));

        panel.add(new JLabel("Nombre:"));
        txtNombre = new JTextField();
        panel.add(txtNombre);

        panel.add(new JLabel("Edad:"));
        txtEdad = new JTextField();
        panel.add(txtEdad);

        JButton btnGuardar = new JButton("Guardar");
        btnGuardar.addActionListener(e -> {
            pasajero.setNombre(txtNombre.getText());
            try {
                pasajero.setEdad(Integer.parseInt(txtEdad.getText()));
                JOptionPane.showMessageDialog(this, "Pasajero actualizado correctamente");
                if (onSaveCallback != null) {
                    onSaveCallback.run(); // refresca tabla
                }
                dispose();
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Edad invÃ¡lida");
            }
        });

        JButton btnCancelar = new JButton("Cancelar");
        btnCancelar.addActionListener(e -> dispose());

        panel.add(btnGuardar);
        panel.add(btnCancelar);

        add(panel);
    }

    private void btnSearchBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchBusActionPerformed
        // TODO add your handling code here:
        String placa = txtSearch.getText();
        if (placa.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Por favor ingrese una placa para buscar.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        Vehiculo v = barbosa.searchVehiculo(placa);

        if (v == null) {
            JOptionPane.showMessageDialog(this,
                    "No se encontrÃ³ ningÃºn vehÃ­culo con la placa " + placa,
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (v instanceof Carro) {
            JOptionPane.showMessageDialog(this,
                    "Error: la placa " + placa + " pertenece a un Carro, no a un Bus.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (v instanceof Bus) {
            busActual = (Bus) v;
            // ðŸ”¹ Llenamos los campos del formulario con los datos del bus encontrado
            txtMarca.setText(busActual.getMarca());
            txtColor.setText(busActual.getColor());
            txtPlaca.setText(busActual.getPlaca());
            txtCombustible.setText(busActual.getCombustible());
            txtModelo.setText(busActual.getModelo());
            txtAnio.setText(String.valueOf(busActual.getAnio()));
            txtEstado.setText(busActual.getEstado());
            boxCTV.setSelectedItem(busActual.getCantidadTelevisores());
            boxBanio.setSelectedItem(busActual.isTieneBanio() ? "Si" : "No");
            boxSP.setSelectedItem(busActual.isTieneSegundoPiso() ? "Si" : "No");

            JOptionPane.showMessageDialog(this,
                    "Bus encontrado y cargado en el formulario.",
                    "Ã‰xito",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchBusActionPerformed


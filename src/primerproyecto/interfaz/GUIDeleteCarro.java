    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String placa = txtSearch.getText();
        if (placa.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Por favor ingrese una placa para buscar.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        Vehiculo v = barbosa.searchVehiculo(placa);
        
        if (v == null) {
            JOptionPane.showMessageDialog(this,
                    "No se encontrÃ³ ningÃºn vehÃ­culo con la placa " + placa,
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(v instanceof Bus){
            JOptionPane.showMessageDialog(this,
                    "Error: la placa " + placa + " pertenece a un Carro, no a un Bus.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
            
        }
        
        if (v instanceof Carro) {
            carroActual = (Carro) v;
            txtPlaca.setText(carroActual.getPlaca());
            txtMarca.setText(carroActual.getMarca());
            txtAnio.setText(carroActual.getAnio() + "");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

